export function centimeterToMeter(value) {
    return value * 0.01
}

export function centimeterToKilometer(value) {
    return value / 100000
}

export function centimeterToMillimeter(value) {
    return value * 10
}

export function centimeterToFeet(value) {
    return value / 30.48
}

export function centimeterToYard(value) {
    return value / 91.44
}

export function centimeterToInch(value) {
    return value / 2.54
}

export function centimeterToMile(value) {
    return value / 160900
}