export function feetToMeter(value) {
    return value / 3.281
}

export function feetToKilometer(value) {
    return value / 3281
}

export function feetToCentimeter(value) {
    return value * 30.48
}

export function feetToMillimeter(value) {
    return value * 304.8
}

export function feetToYard(value) {
    return value / 3
}

export function feetToInch(value) {
    return value * 12
}

export function feetToMile(value) {
    return value / 5280
}