export function inchToMeter(value) {
    return value / 39.37
}

export function inchToKilometer(value) {
    return value / 39370 
}

export function inchToCentimeter(value) {
    return value * 2.54
}

export function inchToMillimeter(value) {
    return value * 25.4
}

export function inchToFeet(value) {
    return value / 12
}

export function inchToYard(value) {
    return value / 36
}

export function inchToMile(value) {
    return value / 63360
}