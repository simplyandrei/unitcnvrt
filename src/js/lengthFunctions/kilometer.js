export function kilometerToMeter(value) {
    return value * 1000
}

export function kilometerToCentimeter(value) {
    return value * 100000
}

export function kilometerToMillimeter(value) {
    return value * (1*(10^6))
}

export function kilometerToFeet(value) {
    return value * 3281
}

export function kilometerToYard(value) {
    return value * 1094
}

export function kilometerToInch(value) {
    return value * 39370
}

export function kilometerToMile(value) {
    return value / 1.609
}