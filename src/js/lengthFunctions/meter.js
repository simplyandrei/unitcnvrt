export function meterToCentimeter(value) {
    return value * 100
}

export function meterToKilometer(value) {
    return value / 1000
}

export function meterToMilimeter(value) {
    return value * 1000
}

export function meterToFeet(value) {
    return value * 3.281
}

export function meterToYard(value) {
    return value * 1.094
}

export function meterToInch(value) {
    return value * 39.37
}

export function meterToMile(value) {
    return value * 1609
}