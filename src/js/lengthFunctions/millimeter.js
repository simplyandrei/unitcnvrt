export function millimeterToMeter(value) {
    return value / 1000
}

export function millimeterToKilometer(value) {
    return value / (1 * (10 ^ 6))
}

export function millimeterToCentimeter(value) {
    return value / 10
}

export function millimeterToFeet(value) {
    return value / 304.8
}

export function millimeterToYard(value) {
    return value / 914.4
}

export function millimeterToInch(value) {
    return value / 25.4
}

export function millimeterToMile(value) {
    return value / (1.609 * (10 ^ 6))
}