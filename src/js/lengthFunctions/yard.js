export function yardToMeter(value) {
    return value / 1.094
}

export function yardToKilometer(value) {
    return value / 1094
}

export function yardToCentimeter(value) {
    return value * 91.44
}

export function yardToMillimeter(value) {
    return value * 914.4
}

export function yardToFeet(value) {
    return value * 3
}

export function yardToInch(value) {
    return value * 36
}

export function yardToMile(value) {
    return value / 1760
}