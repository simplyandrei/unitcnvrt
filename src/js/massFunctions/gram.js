export function gramToMilligram(value) {
    return value * 1000
}

export function gramToKilogram(value) {
    return value / 1000
}

export function gramToOunce(value) {
    return value / 28.35
}

export function gramToPound(value) {
    return value / 453.6
}

export function gramToTon(value) {
    return value / 907200
}