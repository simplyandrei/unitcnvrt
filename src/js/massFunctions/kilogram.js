export function kilogramToGram(value) {
    return value * 1000
}

export function kilogramToMilligram(value) {
    return value * (1 * (10 ^ 6))
}

export function kilogramToOunce(value) {
    return value * 35.274
}

export function kilogramToPound(value) {
    return value * 2.205
}

export function kilogramToTon(value) {
    return value / 907.2
}