export function milligramToGram(value) {
    return value / 1000
}

export function milligramToKilogram(value) {
    return value / (1 * (10 ^ 6))
}

export function milligramToOunce(value) {
    return value / 28350
}

export function milligramToPound(value) {
    return value / 453600
}

export function milligramToTon(value) {
    return value / (9.072 * (10 ^ 8))
}