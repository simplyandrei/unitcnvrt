export function ounceToGram(value) {
    return value * 28.35
}

export function ounceToMilligram(value) {
    return value * 28350
}

export function ounceToKilogram(value) {
    return value / 35.274
}

export function ounceToPound(value) {
    return value / 16
}

export function ounceToTon(value) {
    return value / 32000
}