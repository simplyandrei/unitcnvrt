export function poundToGram(value) {
    return value * 453.6
}

export function poundToMilligram(value) {
    return value * 453600
}

export function poundToKilogram(value) {
    return value / 2.205
}

export function poundToOunce(value) {
    return value * 16
}

export function poundToTon(value) {
    return value / 2000
}