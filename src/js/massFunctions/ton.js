export function tonToGram(value) {
    return value * 907200
}

export function tonToMilligram(value) {
    return value * (9.072 * (10 ^ 8))
}

export function tonToKilogram(value) {
    return value * 907.2
}

export function tonToOunce(value) {
    return value * 32000
}

export function tonToPound(value) {
    return value * 2000
}